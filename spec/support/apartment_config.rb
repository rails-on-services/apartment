# frozen_string_literal: true

# spec/support/apartment_config.rb

RSpec.configure do |config|
  config.before(:suite) do
    # Set default Apartment configuration for testing
    Apartment.configure do |c|
      c.excluded_models = []
      c.use_schemas = true
      c.seed_after_create = false
      c.default_tenant = nil
    end
  end

  config.after(:suite) do
    Apartment.reset
  end
end
